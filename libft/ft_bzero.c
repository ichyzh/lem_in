#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*t1;
	size_t			i;

	t1 = (unsigned char*)s;
	i = 0;
	while (i < n)
	{
		t1[i] = '\0';
		i++;
	}
}

#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t len)
{
	size_t	dst_len;
	size_t	src_len;

	dst_len = ft_strlen(dst);
	src_len = ft_strlen(src);
	if (len <= dst_len + 1)
		return (src_len + len);
	else
		ft_strncat(dst, src, len - dst_len - 1);
	return (src_len + dst_len);
}
